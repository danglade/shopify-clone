import type { Metadata } from "next";
import { Inter } from "next/font/google";
import "./globals.css";
import Header from "@/components/Header";
import Navigation from "@/components/Navigation";
import { getNavigationData } from "./actions/categories";
import { getSetting } from "./actions/settings";
import AnnouncementHeader from "@/components/AnnouncementHeader";

const inter = Inter({ subsets: ["latin"] });

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const [
    navigationData,
    announcementHtml,
    announcementDismissible,
    isHeaderSticky,
  ] = await Promise.all([
    getNavigationData(),
    getSetting("announcement_bar_html"),
    getSetting("announcement_dismissible"),
    getSetting("sticky_header").then((val) => val === "true"),
  ]);

  return (
    <html lang="en">
      <body className={inter.className}>
        {announcementHtml && (
          <AnnouncementHeader
            htmlContent={announcementHtml}
            isDismissible={announcementDismissible === "true"}
          />
        )}
        <Header isSticky={isHeaderSticky} />
        <div>
          <div className="border-t bg-white sticky top-0 z-40 shadow-md">
            <div className="container mx-auto flex justify-center">
              <Navigation navigationData={navigationData} />
            </div>
          </div>
        </div>
        <main>{children}</main>
      </body>
    </html>
  );
}
